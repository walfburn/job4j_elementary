package ru.job4j.math;

public class MathFunction {

    public static double sum(double one, double two) {
        return one + two;
    }

    public static double multiple(double one, double two) {
        return one * two;
    }

    public static double subtract(double one, double two) {
        return one - two;
    }

    public static double segment(double one, double two) {
        return one / two;
    }
}
