package ru.job4j.condition;

public class Liken {

    public static void main(String[] args) {
        int one = 20;
        int two = 15;
        boolean result = one > two;
        System.out.println(result);
        result = one < two;
        System.out.println(result);
        result = one == two;
        System.out.println(result);
    }
}
